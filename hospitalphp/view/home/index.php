        <h2><strong>Welkom bij dierenziekenhuis de Wae - Medewerker Dashboard</strong></h2>
        <p id="pIndex">Dit is het dashboard voor alle administratieve zaken voor cliënten, patiënten en species. In dierenziekenhuis de Wae verwelkomen wij elk soort dier. Dat kan variëren van eenden tot kikkers, zo gek als je maar kan bedenken. Alle dieren die wij verwelkomen worden 100% goed verzorgt en behandeld. Hier een korte uitleg hoe het systeem werkt: In het tabje <strong>cliënts</strong> verwerken wij de informatie van de baasjes van hun dieren. In het tabje <strong>patiënts</strong> verwerken wij alle dieren die wij op dat moment behandelen, ook worden de dieren gekoppelt aan hun baasjes. In het tabje <strong>species</strong> verwerken wij de soorten dieren.</p>
